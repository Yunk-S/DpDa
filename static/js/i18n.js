// Auto-translation engine: walks text nodes and replaces Chinese with English
// when currentLang === 'en'. Chinese mode is the original text (no-op).
// ============================================================================
(function() {
  if (!window.I18N) return;
  const dict = window.I18N.en;  // en dictionary: zh -> en
  const SKIP_TAGS = new Set(['SCRIPT', 'STYLE', 'CODE', 'PRE', 'NOSCRIPT']);

  function translateText(text) {
    if (!text || !dict) return null;
    // Direct full match first (most common case for short UI strings)
    if (dict[text] !== undefined) return dict[text];
    // Greedy longest-match for substrings (e.g. "中风预测" inside larger text)
    // Sort keys by length desc
    const keys = Object.keys(dict);
    let result = text;
    let changed = false;
    for (const key of keys) {
      if (key.length >= 2 && result.indexOf(key) !== -1) {
        result = result.split(key).join(dict[key]);
        changed = true;
      }
    }
    return changed ? result : null;
  }

  function walkAndTranslate(root) {
    if (!root) return;
    if (SKIP_TAGS.has(root.nodeName)) return;
    // Process text nodes
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode: (node) => {
        if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        // Skip text inside script/style (defense)
        let p = node.parentNode;
        while (p) {
          if (SKIP_TAGS.has(p.nodeName)) return NodeFilter.FILTER_REJECT;
          p = p.parentNode;
        }
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    const nodes = [];
    let n;
    while ((n = walker.nextNode())) nodes.push(n);
    for (const node of nodes) {
      const translated = translateText(node.nodeValue);
      if (translated !== null) node.nodeValue = translated;
    }
  }

  // Expose for global use
  window.dpdaTranslate = walkAndTranslate;
})();
